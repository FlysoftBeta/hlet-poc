def generate_dataset(
    templates,
    modifiers,
    num_records=100000,
):
    datasets = []

    for _ in range(num_records):
        while True:
            template = random.choice(templates)
            if random.randint(0, 100) > 70:
                if "请" in template:
                    continue
                template = random.choice(modifiers).format(
                    template=template.replace("？", "")
                    .replace("。", "")
                    .replace("吗", "")
                )
            break

        mu = 50
        sigma = 20
        if "年" in template:
            mu = 2000
            sigma = 100
        elif "%" in template:
            mu = 50
            sigma = 30

        while True:
            digit = max(min(int(random.gauss(mu, sigma)), max_value), 1)
            if digit >= 1:
                break

        item = template.format(num=digit)
        datasets.append({"input": item, "target": digit})

    return datasets


